package main

import "fmt"

func main() {
	// Usando el operador de declaracion corta, asignar los siguientes valores a variables con los identificadores x,y,z
	// 42, "James Bond", true
	// Luego imprime los valores almacenados en esas variables usando
	// Una sola declaracion de la funcion println
	// Multiples declaraciones de println

	x := 42
	y := "James Bond"
	z := true
	fmt.Println(x, y, z)
	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)
}
